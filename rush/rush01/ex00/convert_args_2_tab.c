void	convert_args_2_tab(char **grid, int **tab)
{
	int i;
	int ii;

	i = 0;
	ii = 0;
	while (i < 9)
	{
		ii = 0;
		while (ii < 9)
		{
			if (grid[i + 1][ii] == '.')
				tab[i][ii] = 0;
			else
				tab[i][ii] = grid[i + 1][ii] - '0';
			ii++;
		}
		i++;
	}
}
