#include <stdlib.h>
#include "ft_list.h"

void	ft_list_foreach(t_list *begin_list, void (*f)(void *))
{
	t_list *list;

	list = begin_list;
	while (list != NULL)
	{
		(*f)(list->data);
		list = list->next;
	}
}
