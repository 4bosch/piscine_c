#include <stdlib.h>
#include "ft_list.h"

t_list	*ft_list_at(t_list *begin_list, unsigned int nbr)
{
	unsigned int	i;
	t_list			*list;

	i = -1;
	list = begin_list;
	while (++i < nbr && list != NULL)
		list = list->next;
	return (list);
}
