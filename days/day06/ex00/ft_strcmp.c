int		ft_strcmp(char *s1, char *s2)
{
	int cnt;

	cnt = 0;
	if (s1 == '\0' && s2 == '\0')
		return (0);
	while (s1[cnt] == s2[cnt] && s1[cnt] != '\0' && s2[cnt] != '\0')
		cnt++;
	if (s1[cnt] != s2[cnt])
		return (s1[cnt] - s2[cnt]);
	return (0);
}
