char	*ft_rot42(char *str)
{
	int c;
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c >= 'a' && c <= 'z')
		{
			c += 16;
			if (c > 'z')
				c -= 26;
		}
		if (c >= 'A' && c <= 'Z')
		{
			c += 16;
			if (c > 'Z')
				c -= 26;
		}
		str[i] = c;
		i++;
	}
	return (str);
}
