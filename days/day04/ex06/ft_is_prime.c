int		ft_is_prime(int nb)
{
	int cnt;

	if (nb <= 0 || nb == 1)
		return (0);
	cnt = 2;
	while (cnt < 46341 && cnt < nb)
	{
		if (nb % cnt == 0)
			return (0);
		cnt++;
	}
	if (nb == 4)
		return (0);
	return (1);
}
